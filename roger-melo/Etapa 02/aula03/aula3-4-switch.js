// condicional switch
const grade = 'B'

switch (grade) {
  case 'A': 
    console.log('você tirou um A')
  case 'B':
    console.log('vocé tirou um B')
    break
  case 'C':
    console.log('vocé tirou um C')
    break
  case 'D':
    console.log('vocé tirou um D')
    break
  case 'E':
    console.log('vocé tirou um E')
    break
  default:
    console.log('nota inválida')
}

